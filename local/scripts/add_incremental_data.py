"""
Inject incremental rows into the upstream PostgreSQL data mart for testing
strata's watermark-bounded incremental ingestion against the production-
shape schema described in ``examples/sample_datamart.ddl.sql``.

Three modes mirror the production scenarios:

  --inserts N    : N brand-new fact rows (never seen by strata)
  --updates N    : N existing fact rows touched — ``last_updated_time``
                   advances + a non-PK field flips (forces re-ingest)
  --mixed N      : roughly half inserts, half updates

The fact table is chosen with ``--table``; the default is ``fact_pay_payment``
to preserve the original CLI shape. All four fact tables share
``last_updated_time`` as their watermark column, so the same flags work
across the board:

  --table fact_pay_payment              (default)
  --table fact_as_balance
  --table fact_as_transaction
  --table fact_as_currency_exchange

Every row written gets ``last_updated_time = NOW()`` so the next ingest's
watermark window picks it up. PKs are not specified — every fact has
``id BIGINT GENERATED BY DEFAULT AS IDENTITY``, so Postgres assigns them.

Run inside the spark container so the helper shares its venv with the
ingest job and bootstrap.py.

Usage::

    python local/scripts/add_incremental_data.py --inserts 100
    python local/scripts/add_incremental_data.py --updates 50
    python local/scripts/add_incremental_data.py --mixed 100
    python local/scripts/add_incremental_data.py --table fact_as_balance --inserts 200
    python local/scripts/add_incremental_data.py --table fact_as_transaction --mixed 500
    python local/scripts/add_incremental_data.py --table fact_as_currency_exchange --inserts 90
"""

from __future__ import annotations

import argparse
import os
import random
import sys
from datetime import date, datetime, timedelta, timezone
from decimal import Decimal

import psycopg2
from psycopg2.extras import execute_values


