def update_transactions(cur, n: int) -> int:
    """Touch N existing transactions: bump last_updated_time + flip the
    debit/credit mark.

    Models the "the bank corrected the posting direction" scenario.
    """
    cur.execute(
        """SELECT id FROM data_mart.fact_as_transaction
           ORDER BY random() LIMIT %s""",
        (n,),
    )
    targets = [r[0] for r in cur.fetchall()]
    if not targets:
        return 0

    now = datetime.now(timezone.utc).replace(tzinfo=None)
    cur.execute(
        """UPDATE data_mart.fact_as_transaction
           SET debit_credit_mark = CASE debit_credit_mark
                                     WHEN 'DBIT' THEN 'CRDT'
                                     ELSE 'DBIT' END,
               last_updated_time = %s
           WHERE id = ANY(%s)""",
        (now, targets),
    )
    return cur.rowcount


# ---------------------------------------------------------------------------
# fact_as_currency_exchange — inserts + updates
# ---------------------------------------------------------------------------

def insert_fx_rates(cur, pools: dict, n: int) -> int:
    """Insert N FX rate observations. Each row is a (from, to) pair on
    a random recent date; same-pair duplicates are allowed since the
    natural production behaviour is multiple observations per day."""
    ccys = pools["dim_currency"]
    if len(ccys) < 2:
        return 0

    rows = []
    now = datetime.now(timezone.utc).replace(tzinfo=None)
    for _ in range(n):
        from_id, to_id = random.sample(ccys, 2)
        rate = Decimal(str(round(random.uniform(0.1, 150.0), 10)))
        rows.append((
            random.choice(pools["dim_data_owner"]),     # data_owner_id
            random.choice(pools["dim_date"]),           # rate_date_id
            from_id, to_id, rate,                       # from/to/rate
            now,                                         # last_updated_time
        ))
    execute_values(
        cur,
        """INSERT INTO data_mart.fact_as_currency_exchange (
            data_owner_id, rate_date_id, from_currency_id, to_currency_id,
            exchange_rate, last_updated_time
        ) VALUES %s""",
        rows,
        page_size=500,
    )
    return len(rows)


def update_fx_rates(cur, n: int) -> int:
    """Touch N existing FX rates: bump last_updated_time + nudge the rate.

    Models the "intra-day re-quote" scenario.
    """
    cur.execute(
        """SELECT id FROM data_mart.fact_as_currency_exchange
           ORDER BY random() LIMIT %s""",
        (n,),
    )
    targets = [r[0] for r in cur.fetchall()]
    if not targets:
        return 0

    now = datetime.now(timezone.utc).replace(tzinfo=None)
    nudge = Decimal("1") + Decimal(str(round(random.uniform(-0.01, 0.01), 6)))
    cur.execute(
        """UPDATE data_mart.fact_as_currency_exchange
           SET exchange_rate     = exchange_rate * %s,
               last_updated_time = %s
           WHERE id = ANY(%s)""",
        (nudge, now, targets),
    )
    return cur.rowcount


# ---------------------------------------------------------------------------
# Dispatcher — pick the right insert/update pair from --table
# ---------------------------------------------------------------------------

# Two parallel maps keep the dispatcher uniform across facts. Each tuple
# is (insert_fn, update_fn). Insert fns take pools; update fns don't.
DISPATCH = {
    "fact_pay_payment":          (insert_payments,      update_payments),
    "fact_as_balance":           (insert_balances,      update_balances),
    "fact_as_transaction":       (insert_transactions,  update_transactions),
    "fact_as_currency_exchange": (insert_fx_rates,      update_fx_rates),
}


def parse_args(argv: list[str] | None = None) -> argparse.Namespace:
    """Parse CLI args. Exactly one of --inserts/--updates/--mixed is required
    so the caller's intent is explicit; ``--table`` picks the fact table
    (default ``fact_pay_payment``); ``--label`` lets you tie a batch back to
    a test name in logs; ``--seed`` makes synthetic data reproducible."""
    p = argparse.ArgumentParser(
        description="Inject incremental test data into the production-shape "
                    "data mart (any of the 4 fact tables)",
    )
    p.add_argument(
        "--table", default="fact_pay_payment",
        choices=sorted(TABLE_ALIASES.keys()),
        metavar="TABLE",
        help="Fact table to inject into. Accepts either logical (FACT_PAY_PAYMENT) "
             "or physical (fact_pay_payment) names. Default: fact_pay_payment.",
    )
    g = p.add_mutually_exclusive_group(required=True)
    g.add_argument("--inserts", type=int, help="N new rows")
    g.add_argument("--updates", type=int, help="N existing rows to update")
    g.add_argument("--mixed", type=int,
                   help="Total rows; ~50/50 split between inserts and updates")
    p.add_argument("--seed", type=int, default=None,
                   help="Random seed for reproducible synthetic data")
    p.add_argument("--label", default=None,
                   help="Free-form label printed to the log "
                        "(handy for tying changes to a test name)")
    return p.parse_args(argv)


def main(argv: list[str] | None = None) -> int:
    """Inject rows into Postgres according to the chosen mode + fact table.

    Exits 0 on success. Errors propagate naturally as psycopg2 exceptions —
    there's no recovery to do because this script makes no promises beyond
    "the insert/update either committed or it didn't."
    """
    args = parse_args(argv)
    if args.seed is not None:
        random.seed(args.seed)

    table = TABLE_ALIASES[args.table]
    label = args.label or datetime.now(timezone.utc).strftime("test-%Y%m%dT%H%M%SZ")
    insert_fn, update_fn = DISPATCH[table]

    conn = connect()
    try:
        cur = conn.cursor()
        pools = fk_pools(cur)

        if args.inserts:
            n = insert_fn(cur, pools, args.inserts)
            conn.commit()
            print(f"[inserts] +{n:,} rows into data_mart.{table}  "
                  f"last_updated_time = NOW()  label={label}")
        elif args.updates:
            n = update_fn(cur, args.updates)
            conn.commit()
            print(f"[updates] ~{n:,} rows touched in data_mart.{table}  "
                  f"last_updated_time = NOW()  label={label}")
        elif args.mixed:
            half = args.mixed // 2
            n_ins = insert_fn(cur, pools, half)
            conn.commit()
            n_upd = update_fn(cur, args.mixed - half)
            conn.commit()
            print(f"[mixed]  +{n_ins:,} inserts, ~{n_upd:,} updates in "
                  f"data_mart.{table}  label={label}")
        cur.close()
    finally:
        conn.close()
    return 0


if __name__ == "__main__":
    sys.exit(main())
