def insert_balances(cur, pools: dict, n: int) -> int:
    rows = []
    now = datetime.now(timezone.utc).replace(tzinfo=None)
    for _ in range(n):
        opening = Decimal(str(round(random.uniform(10_000, 5_000_000), 2)))
        rows.append((
            random.choice(pools["dim_pay_bank_status"]),  # balance_type_id
            random.choice(pools["dim_account"]),          # account_id
            random.choice(pools["dim_currency"]),         # currency_id
            random.choice(pools["dim_date"]),             # balance_date_id
            random.choice(pools["dim_data_owner"]),       # data_owner_id
            random.choice(pools["dim_routing"]),          # routing_id
            opening,                                       # balance_amount
            opening,                                       # amount_in_default_currency
            Decimal("1.0000000000"),                      # fx_rate_of_default_currency
            date.today() - timedelta(days=random.randint(0, 30)),  # fx_rate_date
            random.randint(1, 99999),                     # statement_number
            random.randint(1, 9999),                      # sequence_number
            None, None,                                   # account_statement_id, bank_transaction_id
            None, None, None, None, None,                 # custom_text1-5
            now,                                          # last_updated_time
        ))
    execute_values(
        cur,
        """INSERT INTO data_mart.fact_as_balance (
            balance_type_id, account_id, currency_id, balance_date_id,
            data_owner_id, routing_id, balance_amount,
            amount_in_default_currency, fx_rate_of_default_currency,
            fx_rate_date, statement_number, sequence_number,
            account_statement_id, bank_transaction_id,
            custom_text1, custom_text2, custom_text3, custom_text4, custom_text5,
            last_updated_time
        ) VALUES %s""",
        rows,
        page_size=500,
    )
    return len(rows)


def update_balances(cur, n: int) -> int:
    """Touch N existing balances: bump last_updated_time + nudge balance_amount.

    Models the "end-of-day repricing changed the carried balance" scenario.
    """
    cur.execute(
        """SELECT id FROM data_mart.fact_as_balance
           ORDER BY random() LIMIT %s""",
        (n,),
    )
    targets = [r[0] for r in cur.fetchall()]
    if not targets:
        return 0

    now = datetime.now(timezone.utc).replace(tzinfo=None)
    cur.execute(
        """UPDATE data_mart.fact_as_balance
           SET balance_amount             = balance_amount * 1.001,
               amount_in_default_currency = amount_in_default_currency * 1.001,
               last_updated_time          = %s
           WHERE id = ANY(%s)""",
        (now, targets),
    )
    return cur.rowcount


