# Logical name → physical table name. Allowing lowercase + UPPERCASE means the
# CLI accepts both the strata-style logical name (FACT_PAY_PAYMENT) and the
# Postgres table name (fact_pay_payment).
TABLE_ALIASES = {
    "fact_pay_payment":          "fact_pay_payment",
    "FACT_PAY_PAYMENT":          "fact_pay_payment",
    "fact_as_balance":           "fact_as_balance",
    "FACT_AS_BALANCE":           "fact_as_balance",
    "fact_as_transaction":       "fact_as_transaction",
    "FACT_AS_TRANSACTION":       "fact_as_transaction",
    "fact_as_currency_exchange": "fact_as_currency_exchange",
    "FACT_AS_CURRENCY_EXCHANGE": "fact_as_currency_exchange",
}


# ---------------------------------------------------------------------------
# Connection + FK pool helpers
# ---------------------------------------------------------------------------

def connect():
    """Connect to the local Postgres data mart.

    Auto-detects whether we're inside the spark Docker container (uses
    the compose-network hostname ``postgres``) or running on the host
    (uses ``localhost``). Credentials and database name match the
    docker-compose defaults; override via PG* env vars.
    """
    default_host = "postgres" if os.path.exists("/.dockerenv") else "localhost"
    return psycopg2.connect(
        host=os.environ.get("PGHOST", default_host),
        port=os.environ.get("PGPORT", "5432"),
        dbname=os.environ.get("PGDATABASE", "data_mart"),
        user=os.environ.get("PGUSER", "strata"),
        password=os.environ.get("PGPASSWORD", "strata"),
    )


def fk_pools(cur) -> dict:
    """Cache the FK ID pools used across the fact-table seeders.

    Empties default to ``[1]`` rather than raising — that way the inserter
    still works against a partially-seeded data mart, even if it produces
    rows that won't join cleanly downstream.
    """
    pools: dict[str, list[int]] = {}
    for tbl in (
        "dim_account", "dim_currency", "dim_date", "dim_data_owner",
        "dim_classification", "dim_routing", "dim_as_transaction_type",
        "dim_pay_characteristics", "dim_pay_bank_status", "dim_user",
    ):
        cur.execute(f"SELECT id FROM data_mart.{tbl}")
        pools[tbl] = [r[0] for r in cur.fetchall()] or [1]
    return pools


# ---------------------------------------------------------------------------
# fact_pay_payment — inserts + updates
# ---------------------------------------------------------------------------

def _payment_row(pools: dict, now: datetime) -> tuple:
    """Build one synthetic row for fact_pay_payment (no id — IDENTITY)."""
    cc, cn, cgc, cgn, bic = random.choice(COUNTRIES)
    amt = Decimal(str(round(random.uniform(100, 500_000), 2)))
    creation = now - timedelta(
        days=random.randint(0, 30),
        seconds=random.randint(0, 86400),
    )
    return (
        random.choice(pools["dim_account"]),            # ordering_account_id
        random.randint(1, 99999),                       # requesting_execution_date
        random.choice([1, 2, 3, 4, 5]),                 # payment_method_id
        random.choice(pools["dim_currency"]),           # currency_id
        random.choice(pools["dim_data_owner"]),         # data_owner_id
        random.choice(pools["dim_classification"]),     # classification_id
        random.choice(pools["dim_pay_characteristics"]), # characteristics_id
        random.choice(pools["dim_user"]),               # first_approver_id
        random.choice(pools["dim_user"]),               # second_approver_id
        random.choice(pools["dim_pay_bank_status"]),    # bank_status_id
        random.choice(pools["dim_routing"]),            # routing_id
        amt,                                             # amount
        amt,                                             # amount_in_default_currency
        Decimal("1.0000000000"),                        # fx_rate_of_default_currency
        f"AC{random.randint(10_000_000, 99_999_999)}",  # counter_account_number
        f"Counter Bank {random.randint(1, 100)}",       # counter_bank_name
        cc, cn,                                         # counter_bank_country_*
        cc, cn, cgc, cgn,                               # counterparty_country_*
        random.randint(1, 99999),                       # own_reference_id
        random.randint(1, 9999),                        # own_reference_batch_id
        f"REF-{random.randint(1_000_000, 9_999_999)}",  # own_reference_first_id
        None, None,                                     # own_reference_second/third_id
        creation,                                       # amount_format_date
        creation + timedelta(minutes=30),               # amount_approval_date
        creation + timedelta(hours=1),                  # amount_release_date
        creation + timedelta(minutes=15),               # first_approval_date
        creation + timedelta(minutes=25),               # second_approval_date
        creation + timedelta(minutes=10),               # first_signed_date
        creation + timedelta(minutes=20),               # second_signed_date
        creation,                                       # creation_date
        random.choice(pools["dim_user"]),               # creation_user_id
        None,                                            # rejection_reason_description
        None, None, None, None, None,                   # custom_text1-5
        now,                                             # last_updated_time
    )


def insert_payments(cur, pools: dict, n: int) -> int:
    rows = []
    now = datetime.now(timezone.utc).replace(tzinfo=None)
    for _ in range(n):
        rows.append(_payment_row(pools, now))
    execute_values(
        cur,
        """INSERT INTO data_mart.fact_pay_payment (
            ordering_account_id, requesting_execution_date,
            payment_method_id, currency_id, data_owner_id,
            classification_id, characteristics_id,
            first_approver_id, second_approver_id, bank_status_id,
            routing_id, amount, amount_in_default_currency,
            fx_rate_of_default_currency,
            counter_account_number, counter_bank_name,
            counter_bank_country_code, counter_bank_country_name,
            counterparty_country_code, counterparty_country_name,
            counterparty_country_group_code, counterparty_country_group_name,
            own_reference_id, own_reference_batch_id,
            own_reference_first_id, own_reference_second_id, own_reference_third_id,
            amount_format_date, amount_approval_date, amount_release_date,
            first_approval_date, second_approval_date,
            first_signed_date, second_signed_date,
            creation_date, creation_user_id, rejection_reason_description,
            custom_text1, custom_text2, custom_text3, custom_text4, custom_text5,
            last_updated_time
        ) VALUES %s""",
        rows,
        page_size=300,
    )
    return len(rows)


def update_payments(cur, n: int) -> int:
    """Touch N existing payments: bump last_updated_time + flip bank_status_id.

    Mirrors the realistic "the clearing bank reported a status change" path.
    Picks rows at random with no clustering so the test surface stays uniform.
    """
    cur.execute("SELECT id FROM data_mart.dim_pay_bank_status")
    bank_status_ids = [r[0] for r in cur.fetchall()] or [1]

    cur.execute(
        """SELECT id FROM data_mart.fact_pay_payment
           ORDER BY random() LIMIT %s""",
        (n,),
    )
    targets = [r[0] for r in cur.fetchall()]
    if not targets:
        return 0

    now = datetime.now(timezone.utc).replace(tzinfo=None)
    cur.execute(
        """UPDATE data_mart.fact_pay_payment
           SET bank_status_id    = %s,
               last_updated_time = %s
           WHERE id = ANY(%s)""",
        (random.choice(bank_status_ids), now, targets),
    )
    return cur.rowcount


